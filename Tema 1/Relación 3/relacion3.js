function tellFortune(numberOfChildren, partner, location, jobTitle) {
	document.write("<p>You will be a " + jobTitle + " in " + location + ", and married to " + partner + " with " + numberOfChildren + " kids.</p>");
}

tellFortune(3,"Ana", "Australia", "Web Developer");
tellFortune(2,"Cristina", "Spain", "System Engineer");
tellFortune(1,"Sophie", "New Zeland", "Teacher");